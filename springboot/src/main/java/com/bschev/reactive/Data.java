package com.bschev.reactive;

public class Data {

    private long id;
    private String value;
    private boolean active;
    private String[] props;

    public long getId() {
        return id;
    }

    public String getValue() {
        return value;
    }

    public boolean isActive() {
        return active;
    }

    public String[] getProps() {
        return props;
    }
}
